//Suma
let resultadoSuma = 10 + 5;
console.log(resultadoSuma); //Output: 15

//Resta
let resultadoResta = 20 - 7;
console.log(resultadoResta); //Output: 13

//Multiplicación
let resultadoMultiplicacion = 3 * 4;
console.log(resultadoMultiplicacion); //Output: 12

//División
let resultadoDivision = 16 / 4;
console.log(resultadoDivision); //Output: 4

//Residuo
let resultadoResiduo = 10 % 3;
console.log(resultadoResiduo); //Output: 1

//Incremento
let numero = 1;
numero++;
console.log(numero); //Output: 2

//Decremento
let otroNumero = 5;
otroNumero--;
console.log(otroNumero); //Output: 4